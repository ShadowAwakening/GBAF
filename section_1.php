    <h1>Bienvenu sur l'extranet du GBAF !</h1>

    <p><strong>Le groupement Banques et Assurances Française</strong> est un conglomérat des banques et assurances les
        plus prestigieuse de France. Face à la multiplicité des acteurs financiers externes proposant des partenariats
        au groupe <strong>nous avons décider de mettre en place un sytème d'évaluation de la qualité de ces
            entreprises.</strong>
        <p>Vous avez donc dés maintenant accès à l'extranet GBAF pour nous <strong>aider à cibler les partenaires
                financiers dans lesquels vous avez le plus confiances.</strong></p>
        <p> Vous trouverez en partie principale la liste des entreprises financières partenaires et vous pourrez
            <strong>donner votre avis</strong>sur ces dernières.</p>
        <p><strong>Vous pouvez cliquer sur la fiche de l'entreprise pour avoir une description plus détaillée et vous
                avez également la possibilité de "liker/disliker" l'entreprise</strong>.</p>
        <p><strong>Pour finir, vous pouvez laisser des commentaires et lire les commentaires d'autre colaborateurs ayant
                donner leurs avis.</strong></p>

        <p>Excellente journée ! :)</p>
